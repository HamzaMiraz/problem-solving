#include <stdio.h>
///Header File / Header Library

int main()
{
    ///Data Type
    int a;
    long long p;
    double x;

    a = 10;
    p = 1234567890123L;

    x = 3.14159;

    printf("Print Integer# a = %d\n",a);
    printf("Print long long# a = %lld\n",p);
    printf("Print Double# a = %lf\n",x);




    return 0;
}
